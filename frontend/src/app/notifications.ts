import { toast } from "react-toastify";

export const notifyError = (mess:string) => {
    toast.error(mess, {
        position: "bottom-right",
        autoClose: 5000,
        hideProgressBar: false,
        closeOnClick: true,
        pauseOnHover: true,
        draggable: true,
        progress: undefined,
        theme: "light",
    });
}

